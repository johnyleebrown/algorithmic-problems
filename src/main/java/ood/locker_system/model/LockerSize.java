package ood.locker_system.model;

public class LockerSize {

	private String lockerSizeId;
	private String title;
	private int length, width, height;
	private int orderId;

	public String getLockerSizeId() {
		return lockerSizeId;
	}

	public String getTitle() {
		return title;
	}

	public int getLength() {
		return length;
	}

	public int getWidth() {
		return width;
	}

	public int getHeight() {
		return height;
	}

	public int getOrderId() {
		return orderId;
	}
}
